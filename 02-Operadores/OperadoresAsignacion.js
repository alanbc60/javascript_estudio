//1. Asignacion (=): Asigna un valor a una variable
let x = 5;
x = 10;
console.log(x);


// Suma y Asignacion (+=): Suma un valor a una variable
let y = 5;
y += 10;
console.log(y);


// Resta y Asignacion (-=): Resta un valor a una variable
let z = 5;
z -= 10;
console.log(z);


// Multiplicacion y Asignacion (*=): Multiplica un valor a una variable
let a = 5;
a *= 10;
console.log(a);

// Division y Asignacion (/=): Divide un valor a una variable
let b = 5;
b /= 10;
console.log(b);

// Modulo y Asignacion (%=): Divide un valor a una variable
let c = 5;
c %= 10;
console.log(c);

