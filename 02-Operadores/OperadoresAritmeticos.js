//1. Suma 

let a = 10;
let b = 5;

//1. Suma
console.log("==== Suma ====");
let suma = a + b;
console.log(suma);

//2. Resta
console.log("==== Resta ====");
let resta = a - b;
console.log(resta);

//3. Multiplicacion
console.log("==== Multiplicacion ====");
let multiplicacion = a * b;
console.log(multiplicacion);

//4. Division
console.log("==== Division ====");
let division = a / b;
console.log(division);

//5. Modulo
console.log("==== Modulo ====");
let modulo = a % b;
console.log(modulo)

// 6. Potencia
console.log("==== Potencia ====");
let potencia = a ** b;
console.log(potencia)

// 7. PreIncremento
console.log("==== PreIncremento ====");
let incremento = a++;
console.log(incremento);

// 8. Preincremento
console.log("==== PreDecremento ====");
let preincremento = ++a;
console.log(preincremento);

// 9. Decremento
console.log("==== Decremento ====");
let predecremento = --a;
console.log(predecremento);

// 10. PostDecremento
console.log("==== PostDecremento ====");
let postdecremento = a--;
