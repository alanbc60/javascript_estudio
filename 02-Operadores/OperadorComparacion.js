//1. igualdad (==): Compara dos valores y devuelve true o false
let x = 5;
let y = 10;
console.log(x == y);

//2. igualdad estricta (===): Compara dos valores y devuelve true o false
let z = 5;
let a = 10;
console.log(z === a);


//3. desigualdad (!=): Compara dos valores y devuelve true o false
let b = 5;
let c = 10;
console.log(b != c);

//4. desigualdad estricta (!==): Compara dos valores y devuelve true o false
let d = 5;
let e = 10;
console.log(d !== e);

//5. mayor que (>): Compara dos valores y devuelve true o false
let f = 5;
let g = 10;
console.log(f > g);

//6. mayor o igual que (>=): Compara dos valores y devuelve true o false
let h = 5;
let i = 10;
console.log(h >= i);

//7. menor que (<): Compara dos valores y devuelve true o false
let j = 5;
let k = 10;
console.log(j < k);

//8. menor o igual que (<=): Compara dos valores y devuelve true o false
let l = 5;
let m = 10;
console.log(l <= m);
